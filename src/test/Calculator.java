
package test;

public class Calculator {
     
     public double add (double operand1, double operand2){
       return (operand1+operand2);
     }
     public double sub (double operand1, double operand2){
       return (operand1-operand2);
     }
     public double div (double operand1, double operand2){
       return (operand1/operand2);
     }
     public double mul (double operand1, double operand2){
       return (operand1*operand2);
     }
     public double add3 (double operand1, double operand2, double operand3){
       return (operand1+operand2+operand3);
     }   
     public double sub3 (double operand1, double operand2, double operand3){
       return (operand1-operand2-operand3);
     }
     public double mul3 (double operand1, double operand2, double operand3){
       return (operand1*operand2*operand3);
     }
     public double div3 (double operand1, double operand2, double operand3){
       return (operand1/operand2/operand3);
     }
}